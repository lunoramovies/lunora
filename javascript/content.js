// ==================== PLAYER FUNCTIONS ====================
function playMovie(title, url) {
  movieTitle.textContent = title;
  movieFrame.src = url;
  episodeList.innerHTML = '';
  episodeControls.classList.add("hidden");

  // === Download Button Logic ===
  const movieData = Object.values(movies).find(m => m.title === title);
  const downloadBtn = document.getElementById("downloadMovie");

  if (movieData && movieData.download) {
    downloadBtn.href = movieData.download;
    downloadBtn.classList.remove("hidden");
  } else {
    downloadBtn.classList.add("hidden");
  }
}

const downloadSeasonBtn = document.getElementById("downloadSeason");

function updateDownloadSeasonButton(showData, selectedSeason) {
  const seasonDownloadKey = `download${selectedSeason}`;
  const downloadLink = showData[seasonDownloadKey];

  if (downloadLink) {
    downloadSeasonBtn.onclick = () => window.open(downloadLink, "_blank");
    downloadSeasonBtn.classList.remove("opacity-40");
    downloadSeasonBtn.disabled = false;
  } else {
    downloadSeasonBtn.onclick = null;
    downloadSeasonBtn.classList.add("opacity-40");
    downloadSeasonBtn.disabled = true;
  }
}

function loadEpisodes(title, episodes) {
  movieTitle.textContent = title;
  episodeControls.classList.remove("hidden");

  const seasons = [...new Set(Object.keys(episodes).map(ep => ep.split(" ")[1]))];

  seasonSelector.innerHTML = '';
  seasons.forEach(season => {
    const option = document.createElement('option');
    option.value = season;
    option.textContent = `Season ${season}`;
    seasonSelector.appendChild(option);
  });

  seasonSelector.onchange = () => {
  renderEpisodesForSeason(episodes, seasonSelector.value, title);

  const currentShowData = Object.values(movies).find(
    m => m.type === "show" && m.title === title
  );
  if (currentShowData) {
    updateDownloadSeasonButton(currentShowData, seasonSelector.value);
  }
};


  seasonSelector.value = seasons[0];
  renderEpisodesForSeason(episodes, seasons[0], title);
}

function renderEpisodesForSeason(episodes, selectedSeason, title) {
  episodeList.innerHTML = '';
  currentEpisodes = Object.entries(episodes).filter(([epTitle]) =>
    epTitle.includes(`Season ${selectedSeason}`)
  );

  // Find the current show data
const currentShowData = Object.values(movies).find(
  m => m.type === "show" && m.title === title
);

// Update the download button for this season
if (currentShowData) {
  updateDownloadSeasonButton(currentShowData, selectedSeason);
}

  currentEpisodes.forEach(([epTitle, epUrl]) => {
    const epBtn = document.createElement('button');
    // Extract episode number for label
    let match = epTitle.match(/Episode (\d+)/i);
    let label = match ? `Episode ${match[1]}` : epTitle;
    epBtn.textContent = label;
    epBtn.className = 'bg-zinc-900 hover:bg-zinc-800 text-white px-2.5 py-1.5 rounded-md border border-zinc-700 text-sm transition whitespace-nowrap';
    epBtn.onclick = () => {
      movieTitle.textContent = `${title}  ${epTitle}`;
      movieFrame.src = epUrl;
      currentEpisodeKey = epTitle;
      updateEpisodeNav();
    };
    episodeList.appendChild(epBtn);
  });

  if (currentEpisodes.length > 0) {
    const [firstTitle, firstUrl] = currentEpisodes[0];
    movieTitle.textContent = `${title}  ${firstTitle}`;
    movieFrame.src = firstUrl;
    currentEpisodeKey = firstTitle;
    updateEpisodeNav();

    const currentShowData = Object.values(movies).find(
  m => m.type === "show" && m.title === title
);
if (currentShowData) {
  updateDownloadSeasonButton(currentShowData, seasons[0]);
   }
  }
}

function updateEpisodeNav() {
  const index = currentEpisodes.findIndex(([ep]) => ep === currentEpisodeKey);

  prevEpisodeBtn.disabled = index <= 0;
  nextEpisodeBtn.disabled = index >= currentEpisodes.length - 1;

  prevEpisodeBtn.onclick = () => {
    if (index > 0) {
      const [title, url] = currentEpisodes[index - 1];
      movieTitle.textContent = title;
      movieFrame.src = url;
      currentEpisodeKey = title;
      updateEpisodeNav();
    }
  };

  nextEpisodeBtn.onclick = () => {
    if (index < currentEpisodes.length - 1) {
      const [title, url] = currentEpisodes[index + 1];
      movieTitle.textContent = title;
      movieFrame.src = url;
      currentEpisodeKey = title;
      updateEpisodeNav();
    }
  };
}

// ==================== SEARCH ====================
searchInput.addEventListener("input", () => {
  const term = searchInput.value.toLowerCase();
  filterButtons("movieList", term);
  filterButtons("showList", term);
});

function filterButtons(containerId, term) {
  const container = document.getElementById(containerId);
  Array.from(container.children).forEach(btn => {
    const visible = btn.textContent.toLowerCase().includes(term);
    btn.style.display = visible ? "block" : "none";
  });
}
