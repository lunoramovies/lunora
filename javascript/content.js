function createMovieButton(data, sidebar = false) {
  const btn = document.createElement('button');
  btn.className = sidebar
    ? 'flex flex-col items-center text-center p-2 rounded-lg bg-zinc-800 hover:bg-zinc-700 transition shadow border border-zinc-700 w-full'
    : 'flex items-center gap-4 text-left p-2 rounded-lg bg-zinc-800 hover:bg-zinc-700 transition shadow border border-zinc-700 w-full';

  const poster = data.poster || 'https://i.ibb.co/f15zJkV/Umovies.png';

  btn.innerHTML = sidebar
    ? `
      <img src="${poster}" alt="${data.title} poster" class="w-full h-32 object-cover rounded-md mb-2" />
      <span class="text-white font-semibold text-sm">${data.title}</span>
    `
    : `
      <img src="${poster}" alt="${data.title} poster" class="w-12 h-16 object-cover rounded-md flex-shrink-0" />
      <span class="text-white font-semibold">${data.title}</span>
    `;

  btn.onclick = () => {
    if (data.type === 'show') {
      loadEpisodes(data.title, data.episodes);
    } else {
      playMovie(data.title, data['Main Movie']);
    }
    closeDrawer();
  };

  return btn;
}

