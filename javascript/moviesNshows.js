    // Select elements
    const movieItems = document.querySelectorAll('.movie-item');
    const episodeFrame = document.getElementById('episodeFrame');
    const movieList = document.querySelector('.movie-list');
    const episodeContainer = document.querySelector('.episode-container');
    const seasonSelector = document.getElementById('seasonSelector');
    const episodeList = document.getElementById('episodeList');
    const movieTitle = document.getElementById('movieTitle'); // Element to display the movie's name
    const viewer = document.getElementById('viewer'); // Element to adjust the position
    
    // Add event listeners to movie items
    movieItems.forEach(item => {
        item.addEventListener('click', function() {
            const movieKey = item.dataset.movie;
            const movieData = movies[movieKey]; // Get movie data from movies object
            
            if (movieData) {
                const movieName = movieData.title || movieKey.replace(/([a-z])([A-Z])/g, '$1 $2').replace(/_/g, ' ');
                movieTitle.textContent = movieName; // Set the movie's name in the sidebar
            }
            
            // Check if movieData contains episodes
            if (movieData && movieData.episodes) {
                populateSeasonSelector(movieData.episodes);
                episodeContainer.style.display = 'block'; // Show the episode container
                movieList.style.display = 'none'; // Hide the movie list
                adjustViewerPosition(); // Adjust the position of the viewer
            } else if (movieData && movieData['Main Movie']) {
                // Handle case for movies with 'Main Movie' property
                episodeFrame.src = movieData['Main Movie']; // Set the src attribute of the episodeFrame
                episodeContainer.style.display = 'block'; // Show the episode container
                movieList.style.display = 'none'; // Hide the movie list
                adjustViewerPosition(); // Adjust the position of the viewer
                clearEpisodeList(); // Clear the episode list and season selector
            }
        });
    });
    
    function populateSeasonSelector(episodes) {
        seasonSelector.innerHTML = '';
        episodeList.innerHTML = '';
    
        const seasons = Object.keys(episodes).reduce((acc, episode) => {
            const season = episode.split(' ')[1];
            if (!acc.includes(season)) acc.push(season);
            return acc;
        }, []);
    
        seasons.forEach(season => {
            const option = document.createElement('option');
            option.value = season;
            option.textContent = `Season ${season}`;
            seasonSelector.appendChild(option);
        });
    
        seasonSelector.addEventListener('change', function() {
            populateEpisodeList(episodes, this.value);
        });
    
        if (seasons.length > 0) {
            seasonSelector.value = seasons[0];
            populateEpisodeList(episodes, seasons[0]);
        }
    }
    
    function populateEpisodeList(episodes, season) {
        episodeList.innerHTML = '';
    
        Object.keys(episodes).forEach(episode => {
            if (episode.includes(`Season ${season}`)) {
                const li = document.createElement('li');
                li.textContent = episode;
                li.addEventListener('click', function() {
                    episodeFrame.src = episodes[episode];
                });
                episodeList.appendChild(li);
            }
        });

        // Automatically select the first episode of the season
        const firstEpisode = episodeList.querySelector('li');
        if (firstEpisode) {
            firstEpisode.click();
        }
    }
    
    function clearEpisodeList() {
        seasonSelector.innerHTML = '';
        episodeList.innerHTML = '';
    }

    function adjustViewerPosition() {
        const sidebarHeight = document.querySelector('.sidebar').offsetHeight;
        viewer.style.marginTop = `-${sidebarHeight / 1}px`; // Adjust the position a bit higher
    }

    // Back to menu functionality
    document.getElementById('backToMenu').addEventListener('click', function(e) {
        e.preventDefault();
        movieList.style.display = 'flex'; // Show movie list again
        episodeContainer.style.display = 'none'; // Hide episode container
        episodeFrame.src = ''; // Clear iframe src
        movieTitle.textContent = ''; // Clear the movie's name in the sidebar
        clearEpisodeList(); // Clear the episode list and season selector
    });